def _pst_index(square: int, color: chess.Color) -> int:
    rank = chess.square_rank(square)
    file = chess.square_file(square)
    if color == chess.WHITE:
        return (7 - rank) * 8 + file
    return rank * 8 + file


def evaluate(board: chess.Board) -> int:
    """Centipawn score from white's perspective. Positive = white winning."""
    if board.is_checkmate():
        return -CHECKMATE_SCORE if board.turn == chess.WHITE else CHECKMATE_SCORE
    if board.is_game_over():
        return 0

    score = 0
    for square in chess.SQUARES:
        piece = board.piece_at(square)
        if piece is None:
            continue
        value = PIECE_VALUES[piece.piece_type] + PST[piece.piece_type][_pst_index(square, piece.color)]
        score += value if piece.color == chess.WHITE else -value
    return score
